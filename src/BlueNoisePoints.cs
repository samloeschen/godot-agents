    void GeneratePointsRoutine() {
        float normalizedRadius = spacing / squareSize;
        int radiusIndex = NearestRadiusIndex(normalizedRadius, out _);
        int numSamples = PACKED_CIRCLE_N[radiusIndex];
        points = new Vector2[numSamples];

        GenerateBlueNoiseSamplePoints(ref points, seed);
    }

    public BlueNoisePoints() { 

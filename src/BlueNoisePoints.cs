    }

    const float DEFAULT_M = 0.25f;

    public static void GenerateBlueNoiseSamplePoints(ref Vector2[] array, uint seed, float m = DEFAULT_M) {
        if (array == null) {
            throw new NullReferenceException("The array passed to GenerateBlueNoiseSamplePoints() is null");
        }
        int numPoints = array.Length;
        GD.Seed(seed + 1);
        array[0] = new(GD.Randf(), GD.Randf());
        for (int i = 1; i < numPoints; i++) {
            float farthest = 0f;
            int candidates = (int)Mathf.Max(1, m * i);
            Vector2 bestPos = Vector2.Zero;
            for (int j = 0; j < candidates; j++) {
                Vector2 candidatePos = new(GD.Randf(), GD.Randf());
                float minDist = Mathf.Inf;
                for (int k = 0; k < i; k++) {
                    float dist = ToroidalDistance(candidatePos, array[k]);
                    if (dist < minDist) {
                        minDist = dist;
                    }
                }
                if (minDist >= farthest) {
                    farthest = minDist;
                    bestPos = candidatePos;
                }
            }
            array[i] = bestPos;
        }
    }

    static float ToroidalDistance(Vector2 a, Vector2 b) {
        Vector2 d = (b - a).Abs();
        if (d.X > 0.5f) {
            d.X = 1f - d.X;
        }
        if (d.Y > 0.5f) {
            d.Y = 1f - d.Y;
        }
        return d.Length();
    }


    // the radius are sorted from biggest to smallest,
    // so this is just a backwards (inverted?) binary search
    static int NearestRadiusIndex(double r, out double result) {
        var array = PACKED_CIRCLE_RADII;
        int n = array.Length;
        if (r > array[0]) {
            result = array[0];
            return 0;
        }

        if (r < array[n - 1]) {
            result = array[n - 1];
            return n - 1;
        }
        
        int ClosestIndex(double x, double a, double b, int i, int j) {
            return Mathf.Abs(x - a) < Mathf.Abs(x - b) ? i : j;
        }
        
        int left = 0, right = n, mid = 0;
        while (left < right) {
            mid = (left + right) / 2;
            if (Mathf.Abs(array[mid] - r) < double.Epsilon) {
                result = array[mid];
                return mid;
            }
            if (r < array[mid]) {
                if (mid < n - 1 && r > array[mid - 1]) {
                    int index = ClosestIndex(r, array[mid], array[mid - 1], mid, mid + 1);
                    result = array[index];
                    return index;
                }
            left = mid + 1;
            }
            else {
                if (mid > 0 && r < array[mid - 1]) {
                    int index = ClosestIndex(r, array[mid - 1], array[mid], mid - 1, mid);
                    result = array[index];
                    return index;
                }
                right = mid;
            }
        }
        result = array[mid];
        return mid;
    }

